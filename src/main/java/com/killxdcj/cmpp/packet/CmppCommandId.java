public enum CmppCommandId {
    CMPP_CONNECT(0x00000001L),
    CMPP_CONNECT_RESP(0x80000001L),
    CMPP_TERMINATE(0x00000002L),
    CMPP_TERMINATE_RESP(0x80000002L),
    CMPP_SUBMIT(0x00000004L),
    CMPP_SUBMIT_RESP(0x80000004L),
    CMPP_DELIVER(0x00000005L),
    CMPP_DELIVER_RESP(0x80000005L),
    CMPP_QUERY(0x00000006L),
    CMPP_QUERY_RESP(0x80000006L),
    CMPP_CANCEL(0x00000007L),
    CMPP_CANCEL_RESP(0x80000007L),
    CMPP_ACTIVE(0x00000008L),
    CMPP_ACTIVE_RESP(0x80000008L),
    CMPP_FWD(0x00000009L),
    CMPP_FWD_RESP(0x80000009L),
    CMPP_MT_ROUTE(0x00000010L),
    CMPP_MT_ROUTE_RESP(0x80000010L),
    CMPP_MO_ROUTE(0x00000011L),
    CMPP_MO_ROUTE_RESP(0x80000011L),
    CMPP_GET_ROUTE(0x00000012L),
    CMPP_GET_ROUTE_RESP(0x80000012L),
    CMPP_MT_ROUTE_UPDATE(0x00000013L),
    CMPP_MT_ROUTE_UPDATE_RESP(0x80000013L),
    CMPP_MO_ROUTE_UPDATE(0x00000014L),
    CMPP_MO_ROUTE_UPDATE_RESP(0x80000014L),
    CMPP_PUSH_MT_ROUTE_UPDATE(0x00000015L),
    CMPP_PUSH_MT_ROUTE_UPDATE_RESP(0x80000015L),
    CMPP_PUSH_MO_ROUTE_UPDATE(0x00000016L),
    CMPP_PUSH_MO_ROUTE_UPDATE_RESP(0x80000016L);

    private long commandId;

    CmppCommandId(long commandId) {
        this.commandId = commandId;
    }
    public long getCommandId() {
        return commandId;
    }
}
